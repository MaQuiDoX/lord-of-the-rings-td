package Enemigo;

public class Enemigo {
    private int vidaEnemigo;
    private int velocidadEnemigo;
    private int danoEnemigo;
    private int magiaOtorgada;
    private String tipoEnemigo;

    public Enemigo(int vidaEnemigo, int velocidadEnemigo, int danoEnemigo, int magiaOtorgada, String tipoEnemigo){
        this.vidaEnemigo = vidaEnemigo;
        this.velocidadEnemigo = velocidadEnemigo;
        this.danoEnemigo = danoEnemigo;
        this.magiaOtorgada = magiaOtorgada;
        this.tipoEnemigo = tipoEnemigo;
    }
}







